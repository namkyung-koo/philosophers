#include "philo.h"

int	ft_atoi(const char *str)
{
	int	res;

	while ((9 <= *str && *str <= 13) || *str == 32)
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			print_error_msg("Negative numbers are not allowed.");
		str++;
	}
	if (*str == '\0')
		print_error_msg("There are invaild arguments.");
	res = 0;
	while ('0' <= *str && *str <= '9')
	{
		res = res * 10 + (*str - '0');
		str++;
	}
	if (*str != '\0')
		print_error_msg("There are invaild arguments.");
	return (res);
}
